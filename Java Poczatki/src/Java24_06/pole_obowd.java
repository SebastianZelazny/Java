package Java24_06;

public class pole_obowd {

	public static int Pkwadrat (int a)
	{
		return a*a;
	}
	public static int Okwadrat (int a)
	{
		return a*4;
	}
	public static int Pprostokat (int a, int b)
	{
		return a*b;
	}
	public static int Oprostokat (int a, int b)
	{
		return 2*a+2*b;
	}
	public static int PTrojkat (int a, int h)
	{
		return a*h/2;
	}
	
	public static int Otrojkat (int a, int b, int c)
	{
		return a+b+c;
	}
}
